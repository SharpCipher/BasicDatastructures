class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
        Node* node1 = head;
        Node* node2 = head;
        Node* node3 = NULL;
        
        for(int i=0; i<k; i++){
            node3 = node1;
            node1 = node1->next;
        }
        
        if(node1 == NULL)
            return head;
        head = node1;
        while(node1 != NULL && (node1->next != NULL)){
            node1 = node1->next;
        }
        node3->next = NULL;
        node1->next = node2;
        
        return head;
    }
};
    


